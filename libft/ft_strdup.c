#include "libft.h"

char	*ft_strdup(const char *s)
{
	size_t	i;
	char	*new;

	if (s == NULL)
		return (NULL);
	i = 0;
	if (!s)
		return (NULL);
	while (s[i])
		i++;
	new = malloc(sizeof(char) * i + 1);
	if (!new)
		return (0);
	i = 0;
	while (s[i])
	{
		new[i] = s[i];
		i++;
	}
	new[i] = '\0';
	return (new);
}
