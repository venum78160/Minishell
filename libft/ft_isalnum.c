#include "libft.h"

int		ft_isalnum(int c)
{
	if (!((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z')))
	{
		if (!(c >= '0' && c <= '9'))
			return (0);
	}
	printf("isalnum c = |%c|, retour 1\n", c);
	return (1);
}
